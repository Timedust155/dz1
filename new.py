a = input('Введите одно из трех: камень, ножницы, бумага: ')
b = input('Введите одно из трех: камень, ножницы, бумага: ')
c = {'бумага': 1, 'ножницы': 2, 'камень': 3}

m1 = c.get(a)
m2 = c.get(b)

if m1 == m2:
    print('Ничья')
elif (m1 == 1 and m2 == 2) or (m1 == 2 and m2 == 3) or (m1 == 3 and m2 == 1):
    print(f"Тимур победил!")
else:
    print(f"Петя победил!")